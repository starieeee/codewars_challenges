// Write the "square"-function here
// First method
function square(a){
  return a * a;
}
console.log(square(3));

// Second method
function squareTwo(a,b){
    return a ** b;
}
console.log(squareTwo(3,2));

// Third method
function squareThree(a,b){
    return Math.pow(a, b);
}
console.log(squareThree(3,2));
